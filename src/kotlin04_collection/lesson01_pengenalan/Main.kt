package kotlin04_collection.lesson01_pengenalan

/**
 * Pengenalan Collection
 *
 * - Collection adalah hal umum yang biasa dimiliki di bahasa pemrograman,
 *   seperti Java, Python, Ruby dan lain-lain.
 * - Sebuah collection biasanya berisikan sejumlah data, bahkan bisa
 *   kosong.
 * - Kotlin standard library menyediakan data Collection yang sudah bisa
 *   langsung kita gunakan, tanpa tambahan library.
 */
fun pengenalanCollection(): Boolean = true

/**
 * Tipe Collection
 *
 * - Secara garis besar, ada 2 jenis collection di Kotlin:
 *      1. Immutable, collection yang hanya mendukung operasi read only.
 *      2. Mutable, collection turunan immutable, namun mendukung operasi
 *         write juga (add, edit, dan remove).
 *
 * Note:
 *   Secaara default, collection di kotlin adalah immutable.
 */
fun collection(): Boolean = true